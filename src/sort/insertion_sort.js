/**
 * 삽입 정렬
 * 순서를 앞으로 이동하면서 앞 순서의 값이 현재 순서보다 크면 위치 변경
 */

function sortInsertion(arr) {
    let j, tmp;

    // N1
    for (i = 0; i < arr.length - 1; i++) {
        j = i;
        // N2
        while(j >= 0 && arr[j] > arr[j + 1]) {
            tmp = arr[j];
            arr[j] = arr[j + 1];
            arr[j + 1] = tmp;
            j--;
        }
    }
}

const arr = [3, 1, 10, 5, 6, 4, 9, 7, 2, 8];

sortInsertion(arr);
